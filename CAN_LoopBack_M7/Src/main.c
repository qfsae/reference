void CAN1Tx() {
	CAN_TxHeaderTypeDef TxHeader;
	uint8_t *CANMessage = "CANBusM7";
	TxHeader.DLC = 8;
	TxHeader.StdId = 0x65D; // arbitrary standard len addr
	TxHeader.IDE = CAN_ID_STD;
	TxHeader.RTR = CAN_RTR_DATA; // we want to send a data frame not request frame

	uint32_t mailbox;

	if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, CANMessage, &mailbox) != HAL_OK) {
		Error_Handler();
	}

	while (HAL_CAN_IsTxMessagePending(&hcan1, mailbox)); // wait until message has been transmitted
	uint8_t *sucessMsg = "Msg transmit\n";
	HAL_UART_Transmit(&huart1, sucessMsg, strlen(sucessMsg), 10);
}

void CAN1Rx() {
	// based Rx
	CAN_RxHeaderTypeDef RxHeader;
	uint8_t recvMessage[8];
	while (!HAL_CAN_GetRxFifoFillLevel(&hcan1, CAN_RX_FIFO0));

	if (HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &RxHeader, recvMessage) != HAL_OK) {
		Error_Handler();
	}

	uint8_t *successMsg = "received CAN MSG!\n";
	HAL_UART_Transmit(&huart1, successMsg, strlen(successMsg), 10);

	// Now print message contents
	HAL_UART_Transmit(&huart1, recvMessage, strlen(recvMessage), 10);
}

